#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	while (j < (i + 1))
	{
		if (s[j] == c)
			return ((char*)s + j);
		j++;
	}
	return (NULL);
}
