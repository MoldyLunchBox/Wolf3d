char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*p_src;

	i = 0;
	p_src = (char*)src;
	while (p_src[i] != '\0')
	{
		dst[i] = p_src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
